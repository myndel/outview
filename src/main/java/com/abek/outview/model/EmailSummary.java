package com.abek.outview.model;

public class EmailSummary {

	private int		id;
	private String	subject;

	public EmailSummary() {
		super();
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getSubject() {
		return subject;
	}

	public void setSubject(String subject) {
		this.subject = subject;
	}
}
